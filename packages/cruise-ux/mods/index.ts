export default function activate(letta) {
  if (!letta.capabilities?.commands) return;

  const disposers = [];

  for (const def of COMMAND_DEFS) {
    disposers.push(
      letta.commands.register({
        id: def.id,
        description: def.description,
        args: def.args,
        async run(ctx) {
          const input = String(ctx.args ?? "").trim();
          if (!input || ["help", "-h", "--help"].includes(input.toLowerCase())) {
            return { type: "output", output: commandHelp(def.id) };
          }

          if (def.id === "ux-cruise") {
            const result = def.build(input);
            if (result && result.type === "output") return result;
            return { type: "prompt", content: result, systemReminder: true };
          }

          return {
            type: "prompt",
            content: def.build(input),
            systemReminder: true,
          };
        },
      }),
    );
  }

  return () => {
    for (const dispose of disposers.reverse()) dispose();
  };
}
