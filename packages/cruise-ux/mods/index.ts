    "",
    "Frame this UX/UI effort before research, ideation, or specification work. Keep it concise and decision-oriented.",
    "",
    "## Output format",
    "Produce a decision frame with these sections:",
    "",
    "1. **User** — who is the primary user? Include secondary users only if they change the UX decision.",
    "2. **Context** — where/when does this happen? Device, environment, workflow pressure, business/domain setting if relevant.",
    "3. **Job-to-be-done** — what is the user trying to accomplish in plain language?",
    "4. **Decision Needed** — what product/UX decision should this work make possible?",
    "5. **Known Evidence** — facts already known; cite sources if from web/research/docs.",
    "6. **Assumptions** — beliefs not yet proven.",
    "7. **Risks** — usability, safety, technical, compliance, trust, or workflow risks.",
    "8. **Hypotheses** — 2-4 testable UX hypotheses.",
    "9. **Validation Criteria** — what evidence would validate, partially validate, or invalidate each hypothesis?",
    "10. **Smallest Useful Prototype** — the minimal prototype needed to test the decision, not the full product.",
