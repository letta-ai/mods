function buildCruisePrompt(raw) {
  const trimmed = String(raw).trim();
  let autoMode = false;
  let loopMode = false;
  let topic = trimmed;

  const modeMatch = trimmed.match(/^(auto|loop)(?:\s+(.+))?$/i);
  if (modeMatch) {
    const mode = modeMatch[1].toLowerCase();
    const rest = String(modeMatch[2] ?? "").trim();
    const secondModeMatch = rest.match(/^(auto|loop)(?:\s+|$)/i);

    if (secondModeMatch) {
      return {
        type: "output",
        output: cruiseModeErrorText(),
      };
    }

    autoMode = mode === "auto";
    loopMode = mode === "loop";
    topic = rest;
  }

  if (!topic) {
    return {
      type: "output",
      output: commandHelp("ux-cruise"),
    };
  }

  const gateInstruction = autoMode
    ? [
        "## Gate mode: AUTO",
        "Run all pipeline stages sequentially without confirmation gates between stages.",
        "The interview stage still naturally pauses for user answers.",
        "After each non-interview stage, summarize the result in 1-3 lines and proceed.",
        "Do not ask 'should I proceed to the next stage?' between stages.",
      ].join("\n")
    : [
        "## Gate mode: PER-STAGE CONFIRMATION (default)",
        "After each stage, summarize the result and use AskUserQuestion to confirm:",
        "- Continue to next stage",
        "- Adjust and redo this stage",
        "- Skip this stage",
        "- Stop here",
        "Only proceed after user confirmation.",
      ].join("\n");

  const loopInstruction = loopMode
    ? [
        "## Loop mode: REVISION LOOP ENABLED",
        "After Stage 6 (Review), respond based on the verdict:",
        "",
        "### Ready",
        "- Stop the loop and produce the final decision-ready summary.",
        "",
        "### Needs Revision",
        "1. Identify which stage needs revision (Spec, Ideate, Interview, etc.) based on the review findings.",
        "2. Ask the user which stage to revise — recommend one based on the review findings.",
        "3. Loop back to that stage, redo it with the review feedback incorporated.",
        "4. Re-run Review on the revised output.",
        "5. Track iteration history: what changed in each iteration and why.",
        "",
        "### Not Ready",
        "Treat Not Ready as a stronger upstream rollback, not a small polish pass. First classify the root cause, then recommend the earliest stage that must be repaired:",
        "- Decision/user/context unclear → return to Stage 0: Decision Frame.",
        "- Evidence is weak or missing → return to Stage 1: Research.",
        "- User/problem/workflow is unclear → return to Stage 2: Adaptive UX Interview.",
        "- Hypotheses or success/failure signals are weak → return to Stage 3: Problem + Hypothesis + Validation Frame.",
        "- Concept direction is wrong or too narrow → return to Stage 4: Ideation.",
        "- Artifact quality/scope/edge cases/test plan are insufficient → return to Stage 5: UX Spec + Prototype/Test Plan.",
        "Ask for user confirmation before rolling back, unless the user requested auto-style execution in the same message.",
        "",
        "Maximum 3 iterations total. After 3 iterations, stop looping and produce a force-decision recommendation with current evidence, remaining risks, and the smallest next test.",
        "",
        "Loop stop conditions:",
        "- Review verdict is Ready → pipeline complete.",
        "- Review verdict remains Not Ready after rollback attempts → force-decision recommendation.",
        "- User says stop → finish with current state.",
        "- 3 iterations reached → force decision recommendation.",
        "",
        "Iteration log format:",
        "```",
        "Iteration 1: initial pipeline → Review: {verdict} → root cause: {cause} → action: revise/rollback {stage}",
        "Iteration 2: revised {stage} → Review: {verdict} → root cause: {cause} → action: revise/rollback {stage}",
        "Iteration 3: revised {stage} → Review: {verdict} → final decision recommendation",
        "```",
      ].join("\n")
    : "";

  return [
    "# UX Cruise — Decision-to-Evidence Pipeline",
    "",
    `Topic: ${topic}`,
    "",
    gateInstruction,
    loopMode ? ["", loopInstruction].join("\n") : "",
    "",
    "## Pipeline stages",
    "",
    "### Stage 0: Decision Frame",
    "Define user, context, job-to-be-done, decision needed, initial assumptions, risks, hypotheses, validation criteria, and smallest useful prototype.",
    "",
    "### Stage 1: Research",
    "Run structured UX research: competitive/prior-art, user/workflow, technical/platform, and domain/compliance/security constraints if relevant. Save durable research artifacts under `docs/search/` when a concrete project path is known. Cite source URLs.",
    "",
    "### Stage 2: Adaptive UX Interview",
    "Run `/ux-interview` as an adaptive clarification stage. Use Discovery Interview when context is thin, Decision Trace when project context exists and prototype/test decisions are needed, or User Research Protocol when the user needs questions/tasks for real participants. Ask one plain-language question at a time and stop when the next unknown is better specified, deferred, or tested.",
    "",
    "### Stage 3: Problem + Hypothesis + Validation Frame",
    "Synthesize the UX problem, hypotheses, validation criteria, failure signals, and evidence needed before ideation. This keeps concepts tied to decisions instead of opinion.",
    "",
    "### Stage 4: Ideation",
    "Generate three distinct UX concepts. For each, include hypothesis, what the prototype validates, failure signal, user flow, ASCII wireframe, strengths, weaknesses, and best-fit context. Recommend one concept or hybrid.",
    "",
    "### Stage 5: UX Spec + Prototype/Test Plan",
    "Create an English UX spec with scenarios, edge/error states, validation criteria, ASCII wireframes, smallest useful prototype scope, and usability-test plan. Offer to save under `docs/plans/`.",
    "",
    "### Stage 6: UX Review",
    "Run a decision-readiness review using the `/ux-review` rubric. Verdict must be Ready, Needs Revision, or Not Ready. Name blocking gaps and smallest fixes before coding/testing.",
    "",
    "## Execution notes",
    "- In gate mode, confirm after every stage.",
    "- In auto mode, do not ask stage-gate questions, but still ask user questions required by the interview stage.",
    "- In loop mode, gates apply AND the revision loop runs after Stage 6.",
    "- If prior conversation already contains a stage result, build on it instead of redoing from scratch.",
    "- For concrete projects, preserve research in `docs/search/` and specs/plans in `docs/plans/`.",
    "- Separate evidence, assumptions, open questions, risks, and decisions throughout.",
    "- Use the conversation language for discussion; shared docs/specs should be in English unless the user asks otherwise.",
  ].join("\n");
}

function cruiseModeErrorText() {
  return [
    "CruiseUX /ux-cruise — choose one mode only.",
    "",
    "Allowed:",
    "  /ux-cruise <topic>",
    "  /ux-cruise auto <topic>",
    "  /ux-cruise loop <topic>",
    "",
    "Not allowed:",
    "  /ux-cruise auto loop <topic>",
    "  /ux-cruise loop auto <topic>",
    "",
    "Use `auto` for one-pass execution, or `loop` for gated revision cycles. Do not combine them.",
  ].join("\n");
}

